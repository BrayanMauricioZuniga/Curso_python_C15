# La indexazción en python es una forma de realizar un seguimiento de los caracteres 
# de una cadena o de los elementos de una lista y de acceder a ellos.

mi_lista = [1,2,3,4,5,6]
print(*mi_lista)

# Indexación desed el inicio de la lista

print(mi_lista[0])
print(mi_lista[1])
print(mi_lista[2])

#Indexación desdee el fin de la lista

print(mi_lista[-1])
print(mi_lista[-2])
print(mi_lista[-3])

# El metodo len() devuelve la longitud de un objeto, ya sea una lista, cadena, tupla  diccionario

print(len(mi_lista))