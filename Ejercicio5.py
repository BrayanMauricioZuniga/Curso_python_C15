# una condicion if en python es una estructura de decisión
# se estructra de la siguiente manera:
# if sentencia:
#    ejecuta si cumple la sentencia
# else sentencia:
#   ejecuta si no cumple la sentencia


numero=int(input("Digite un número: "))

if numero%2==0:
    print(str(numero) + " es par")
else:
    print(str(numero) + " es impar") 